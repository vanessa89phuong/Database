-- Drop and Create the tables in the database
DROP TABLE votes;
DROP TABLE sponsors;
DROP TABLE opposes;
DROP TABLE contributes;
DROP TABLE senators;
DROP TABLE affected_by;
DROP TABLE corporations;
DROP TABLE states;
DROP TABLE legislation;

CREATE TABLE legislation
(
	legnum          INT NOT NULL,
	CONSTRAINT pk_legnum PRIMARY KEY (legnum)
);

CREATE TABLE states
(
	stname		VARCHAR(2) NOT NULL,
	population	INT NOT NULL,
        CONSTRAINT pk_state PRIMARY KEY (stname)
);

CREATE TABLE corporations
(
	cname		VARCHAR(32) NOT NULL,
	stname		VARCHAR(2) NOT NULL,
	totrev		INT NOT NULL,
        CONSTRAINT pk_corporations PRIMARY KEY (cname),
        CONSTRAINT fk_cor_state FOREIGN KEY (stname) REFERENCES states(stname)
);

CREATE TABLE affected_by
(
	cname		VARCHAR(32) NOT NULL,
        legnum          INT NOT NULL,
	howafctd	VARCHAR(16) NOT NULL,
        CONSTRAINT pk_affected_by PRIMARY KEY (cname,legnum),
	CONSTRAINT fk_aff_cname FOREIGN KEY (cname) REFERENCES corporations(cname),
        CONSTRAINT fk_aff_legnum FOREIGN KEY (legnum) REFERENCES legislation(legnum)
);

CREATE INDEX in_aff_legnum ON affected_by (legnum);

CREATE TABLE senators
(
        sname           VARCHAR(32) NOT NULL,
        party           VARCHAR(16) NOT NULL,
        age             INT NOT NULL,
        gender          VARCHAR(8) NOT NULL,
        stname          VARCHAR(2) NOT NULL,
	CONSTRAINT pk_sname PRIMARY KEY (sname),
	CONSTRAINT fk_sen_state FOREIGN KEY (stname) REFERENCES states(stname)
);

CREATE TABLE contributes
(
     sname           VARCHAR(32) NOT NULL,
     cname           VARCHAR(32) NOT NULL,
     cdate      DATE NOT NULL,
     amt             INT,
     CONSTRAINT pk_conttributes PRIMARY KEY (sname, cname, cdate),
     CONSTRAINT fk_con_sname FOREIGN KEY (sname) REFERENCES senators(sname),
     CONSTRAINT fk_con_cname FOREIGN KEY (cname) REFERENCES corporations(cname)
);

CREATE TABLE opposes(
        sname           VARCHAR(32) NOT NULL,
        legnum          INT NOT NULL,
        CONSTRAINT pk_opposes PRIMARY KEY (sname,legnum),
	CONSTRAINT fk_opp_sname FOREIGN KEY (sname) REFERENCES senators(sname),
	CONSTRAINT fk_opp_legnum FOREIGN KEY (legnum) REFERENCES legislation(legnum)
);

CREATE INDEX in_opp_legnum ON opposes (legnum);

CREATE TABLE sponsors
(
        sname           VARCHAR(32) NOT NULL,
        legnum          INT NOT NULL,
        CONSTRAINT pk_sponsors PRIMARY KEY (sname,legnum),
	CONSTRAINT fk_spo_sname FOREIGN KEY (sname) REFERENCES senators(sname),
	CONSTRAINT fk_spo_legnum FOREIGN KEY (legnum) REFERENCES legislation(legnum)
);

CREATE INDEX in_spo_legnum ON sponsors (legnum);

CREATE TABLE votes
(
        sname           VARCHAR(32) NOT NULL,
        legnum          INT NOT NULL,
        vdate           DATE NOT NULL,
        howvoted        VARCHAR(8) NOT NULL,
        CONSTRAINT pk_votes PRIMARY KEY (sname,legnum,vdate),
	CONSTRAINT fk_vot_sname FOREIGN KEY (sname) REFERENCES senators(sname),
	CONSTRAINT fk_vot_legnum FOREIGN KEY (legnum) REFERENCES legislation(legnum)
);

CREATE INDEX in_vot_legnum ON votes (legnum);
